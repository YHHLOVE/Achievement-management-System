package bean;

public class Course {
	private String coursrId;
	private String courseName;
	private String courseProperty;
	private String courseWay;
	private int courseCredit;
	public Course() {
		
	}
	public String getCoursrId() {
		return coursrId;
	}
	public void setCoursrId(String coursrId) {
		this.coursrId = coursrId;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseProperty() {
		return courseProperty;
	}
	public void setCourseProperty(String courseProperty) {
		this.courseProperty = courseProperty;
	}
	public String getCourseWay() {
		return courseWay;
	}
	public void setCourseWay(String courseWay) {
		this.courseWay = courseWay;
	}
	public int getCourseCredit() {
		return courseCredit;
	}
	public void setCourseCredit(int courseCredit) {
		this.courseCredit = courseCredit;
	}
	
}
